package eu.innovation.engineering.util.featurextractor;


public class IdAndTarget {
  private String id;
  private String target;
  
  public IdAndTarget(String id, String target) {
    super();
    this.id = id;
    this.target = target;
  }

  public String getId() {
    return id;
  }
  
  public void setId(String id) {
    this.id = id;
  }
  
  public String getTarget() {
    return target;
  }
  
  public void setTarget(String target) {
    this.target = target;
  }
  
  
  

}
